const skillAliases = {
  '雷神切り': '雷神斬り',
  '赤備え': '赤備え隊',
  '三河弓兵': '三河弓兵隊',
}

const heroId = (name) => {
  const hero = heroes.find((item) => (item.name_jp || item.name) === name || item.name === name)
  if (!hero) throw new Error(`hero missing: ${name}`)
  return hero.sim_id || hero.id
}

const skillId = (name) => {
  const target = skillAliases[name] || name
  const skill = skills.find((item) =>
    (item.name_jp || item.name) === target
    || item.name === target
    || item.name_jp === name
    || item.name === name)
  if (!skill) throw new Error(`skill missing: ${name}`)
  return skill.sim_id || skill.id || skill.name
}

