export type MessageKey = keyof typeof messages

export function useLocale() {
  const t = (key: MessageKey): string => messages[key] ?? key
  const tf = (key: MessageKey, vars: Record<string, string | number>): string =>
    t(key).replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? ''))

  return {
    t,
    tf,
  }
}
